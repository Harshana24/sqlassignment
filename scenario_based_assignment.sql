CREATE VIEW PROINFO
AS
SELECT O1.PRODUCTNAME,O1.QUANTITY,O2.ORDERNUMBER
FROM ORDER O1
LEFT JOIN ORDERITEM O2
ON O1.ID=O2.ORDERID; 						--query 8--



